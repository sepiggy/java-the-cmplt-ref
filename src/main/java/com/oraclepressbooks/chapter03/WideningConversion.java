public class WideningConversion {
  public static void main(String[] args) {
    byte b = 123;
    short s = 123;
    long l = 123;
    char c = 123;

    System.out.println("b=" + b);
    System.out.println("s=" + s);
    System.out.println("l=" + l);
    System.out.println("c=" + c);
  }
}
